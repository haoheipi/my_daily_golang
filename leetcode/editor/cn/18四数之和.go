func fourSum(nums []int, target int) [][]int {
	sort.Ints(nums)
	n := len(nums)
	res := make([][]int, 0)
	for i := 0; i < n-3; i++ {
		if i > 0 && nums[i] == nums[i-1] {
			continue
		}
		for j := i + 1; j < n-2; j++ {
			if j > i+1 && nums[j] == nums[j-1] {
				continue
			}
			a3 := j + 1
			a4 := n - 1
			// æšä¸¾ b -4 -1 -1 0 1 2
			// éœ€è¦ä¿è¯ b çš„æŒ‡é’ˆåœ¨ c çš„æŒ‡é’ˆçš„å·¦ä¾§
			for a3 < a4 {
				// éœ€è¦å’Œä¸Šä¸€æ¬¡æšä¸¾çš„æ•°ä¸ç›¸åŒ
				if a3 > j+1 && nums[a3] == nums[a3-1] {
					a3++
					continue
				}
				if nums[i]+nums[j]+nums[a3]+nums[a4] > target {
					a4--
				} else if nums[i]+nums[j]+nums[a3]+nums[a4] < target {
					a3++
				} else {
					res = append(res, []int{nums[i], nums[j], nums[a3], nums[a4]})
					a3++
				}
			}

		}
	}
	return res
}

