func threeSum(nums []int) [][]int {
	n := len(nums)
	sort.Ints(nums)
	res := make([][]int, 0)
	for i := 0; i < n-2; i++ {
		if i > 0 && nums[i] == nums[i-1] {
			continue
		}
		if nums[i] > 0 {
			break
		}
		third := n - 1
		second := i + 1
		target := -1 * nums[i]
		// æšä¸¾ b -4 -1 -1 0 1 2
		// éœ€è¦ä¿è¯ b çš„æŒ‡é’ˆåœ¨ c çš„æŒ‡é’ˆçš„å·¦ä¾§
		for second < third {
			// éœ€è¦å’Œä¸Šä¸€æ¬¡æšä¸¾çš„æ•°ä¸ç›¸åŒ
			if second > i+1 && nums[second] == nums[second-1] {
				second++
				continue
			}
			if nums[second]+nums[third] > target {
				third--
			} else if nums[second]+nums[third] < target {
				second++
			} else {
				res = append(res, []int{nums[i], nums[second], nums[third]})
				second++
			}
		}

	}
	return res
}

